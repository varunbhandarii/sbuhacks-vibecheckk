def seed_data():
    print("Starting to seed database...")
    db = SessionLocal()

    try:
        # Check if spaces are already seeded
        count = db.query(Space).count()
        if count > 0:
            print(f"'{Space.__tablename__}' table already has {count} rows. Skipping seed.")
            return

        print(f"Seeding {len(sbu_spaces)} spaces...")

        for space_data in sbu_spaces:
            space = Space(
                id=uuid.uuid4(),  # Generate a new UUID for each space
                name=space_data["name"],
                type=space_data["type"],
                lat=space_data["lat"],
                lon=space_data["lon"]
            )
            db.add(space)

        db.commit()
        print("Successfully seeded spaces!")

    except Exception as e:
        db.rollback()
        print(f"Error seeding data: {e}")
    finally:
        db.close()
        print("Database connection closed.")

